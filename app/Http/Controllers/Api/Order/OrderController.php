     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data = DB::table('orders')
        ->leftJoin('readers', 'orders.reader_id', '=', 'readers.id')
        ->select('orders.*', 'readers.name as reader_name')
        ->get();
        return $data;
    }

